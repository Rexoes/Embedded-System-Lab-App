#define A A_Pin
#define B B_Pin
#define C C_Pin
#define D D_Pin
#define E E_Pin
#define F F_Pin
#define G G_Pin

#define DIGIT_1 DIGIT_1_Pin
uint8_t codes[] = {0x3F, 0x06, 0x5B, 0x4F, 0x66, 0x6D, 0x7D, 0x07, 0x7F, 0x6F};

uint8_t count = 0;

void displayNumber(uint8_t number){
	uint8_t code = codes[number];

	HAL_GPIO_WritePin(GPIOE, A, (code & 0x01) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOE, B, (code & 0x02) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOE, C, (code & 0x04) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOE, D, (code & 0x08) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOE, E, (code & 0x10) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOE, F, (code & 0x20) ? GPIO_PIN_SET : GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOE, G, (code & 0x40) ? GPIO_PIN_SET : GPIO_PIN_RESET);

	HAL_GPIO_WritePin(GPIOE, DIGIT_1, GPIO_PIN_SET);

}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){

	// 500ms periyotlar ile zaman kesmesi.

	if(htim->Instance == TIM6){
		displayNumber(count);
		count++;
		if(count > 9)
		  count = 0;
	}
}

